public class String_Replace {
    public static void main(String[] args) {
        String s = "Hello World!";
        System.out.println(s);

        String s1 = s.replace("H","h");
        System.out.println(s1);

        String s2 = s.replace("l","L");
        System.out.println(s2);


    }
}
