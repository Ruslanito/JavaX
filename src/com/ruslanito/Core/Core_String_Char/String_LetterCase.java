public class String_LetterCase {
    public static void main(String[] args) {
        String s = "Hello World!";
        System.out.println(s);

        String s1 = s.toLowerCase();
        System.out.println(s1);

        String s2 = s.toUpperCase();
        System.out.println(s2);

    }
}
